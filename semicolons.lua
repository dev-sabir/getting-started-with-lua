-- Lua needs no separator between consecutive statements, but we can use a semicolon if we wish. 
-- Line breaks play no role in Lua's syntax; 
-- for instance, the following four chunks are all valid and equivalent:


a= 1 
b=a* 2

a = 1;
b = a * 2;

a = 1; b = a * 2

a = 1  b = a * 2    -- ugly, but valid

-- Use Semicolon only when, write two or more statements in the same line(which we hardly do)
